package com.cooler.cool.Util.Text;

public class TextObj
{
    public TextObj(float x, float y, String text, String font, float size)
    {
        this.x = x;
        this.y = y;
        this.text = text;
        this.font = font;
        this.size = size;
    }

    public float x;
    public float y;
    public String text;
    public String font;
    public float size;
}
